package basicjava;

public class Decreincre1 {
	
	public static void main(String[] args)  {
		
		int a=46, b=56;
		
		System.out.println(a>b);   //greater than
		System.out.println(a<b);    // less than 
		System.out.println(a<=b);   // less than eqals to
		System.out.println(a>=b);    //greater than eqals to
		System.out.println(a==b);    //==operator
		System.out.println(a!=b);     //!=not eqals
		
	}
}
		
		
	
