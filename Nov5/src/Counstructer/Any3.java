package Counstructer;

public class Any3 {
	
	static short test()  {
		
		short a=15;
		short b=65;
		short c=(short)(a+b);
		System.out.println(c);
		return(c);
	}
	static  long demo()   {
		long x=20;
		long y=30;
		long z=x+y;
		System.out.println(z);
		return(z);
	}
	
	static  byte demo1()   {
		byte x=30;
		byte y=30;
		byte p=(byte)(x+y);
		System.out.println(p);
		return(p);
	}
		
		
	public static void main(String[] args)   {
		test();
		demo();
		demo1();
		
	}
}
	
	


