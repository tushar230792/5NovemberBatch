package basicjava;

public class datatype {
	
public static void main(String[] args) {
		
		boolean stdpass=false;  // conditional datatype only true or false outut
		byte stdmarks =100;      //values from -128 to 127 use byte instead of save it
		short totalmarks =500;   // values from -32768 to 32767
		int rollnumber=55657;     // greater than short DT range
		long mobilenumber=9272386516L;        // L is mandatory when we use long 
		double stdavgmarks=567.7987456987456984;   // fractional uoto 15 digit 
		float stdpercentage=78.98f;              // f is mandatory
		char stdgrade='A';
		
		System.out.println(stdpass);
		System.out.println(stdmarks);
		System.out.println(totalmarks);
		System.out.println(rollnumber);
		System.out.println(mobilenumber);
		System.out.println(stdavgmarks);
		System.out.println(stdpercentage);
		System.out.println(stdgrade);
	}
}
	
	
	


